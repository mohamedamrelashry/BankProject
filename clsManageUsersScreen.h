#pragma once

#include <iostream>
#include <iomanip>
#include "clsScreen.h"
#include "clsBankClient.h"
#include "clsInputValidate.h"
#include "clsUsersListScreen.h"
#include "clsClientListScreen.h"
#include "clsAddNewUserScreen.h"
#include "clsDeleteUserScreen.h"
#include "clsUpdateUserScreen.h"
#include "clsFindUserScreen.h"
class clsManageUsersScreen: protected clsScreen 
{
private: 
   
    enum enManageUsersMenueOptions {
        eListUsers = 1, eAddNewUser = 2, eDeleteUser = 3,
        eUpdateUser = 4, eFindUser = 5, eMainMenue = 6
    };

    
    
    static short _ReadManageUsersScreenOption() {
        cout << setw(37) << left << ""
            << "Choose what do you want to do? [1 to 6]? ";
        short Choice =
            clsInputValidate::ReadShortNumberBetween(1, 6, "Enter Number between 1 to 6? ");
        return Choice;
    }
    static void _GoBackToManageUsersMenue() {
        cout << setw(37) << left << ""
            << "\n\tPress any key to go back to Manage users Menue...\n";
        system("pause>0");
        ShowManageUsersScreen();
    }
    static void _ShowListUsersScreen(){
        clsUsersListScreen::ShowUsersList();
    }
    static void _ShowAddNewUsersScreen() {
        clsAddNewUserScreen::ShowAddNewUserScreen();
    }
    static void _ShowDeleteUserScreen() {
        clsDeleteUserScreen::showDeleteClientScreen();
    }
    static void _ShowUpdateUserScreen() {
        clsUpdateUserScreen::ShowUpdateUserScreen();
    }
    static void _ShowFindUserScreen() {
        clsFindUserScreen::ShowFindUserScreen();
    }
    static void _ShowMainMenue() {}

    static void _performManageUsersMenueOption(enManageUsersMenueOptions ManageUsersOptions) {
        switch (ManageUsersOptions)
        {
        case clsManageUsersScreen::eListUsers:
            system("cls");
            _ShowListUsersScreen();
            _GoBackToManageUsersMenue();

            break;
        case clsManageUsersScreen::eAddNewUser:
            system("cls");
            _ShowAddNewUsersScreen();
            _GoBackToManageUsersMenue();

            break;
        case clsManageUsersScreen::eDeleteUser:
            system("cls");
            _ShowDeleteUserScreen();
            _GoBackToManageUsersMenue();

            break;
        case clsManageUsersScreen::eUpdateUser:
            
            system("cls");
            _ShowUpdateUserScreen();
            _GoBackToManageUsersMenue();

            break;
        case clsManageUsersScreen::eFindUser:
            system("cls");
            _ShowFindUserScreen();
            _GoBackToManageUsersMenue();


            break;
        case clsManageUsersScreen::eMainMenue:
            
            break;
        }

    }
public: 

    static void ShowManageUsersScreen() {
        system("cls"); 
        if (!CheckPermissionRights(clsUser::pManageUsers)) {

            return;
        }

        string title = "\t Manage Users Screen";
        _DrawScreenHeader(title); 
        cout << setw(37) << left << "" << "===========================================\n";
        cout << setw(37) << left << "" << "\t\t  Manage Users Menue\n";
        cout << setw(37) << left << "" << "===========================================\n";
        cout << setw(37) << left << "" << "\t[1] List Users.\n";
        cout << setw(37) << left << "" << "\t[2] Add New User.\n";
        cout << setw(37) << left << "" << "\t[3] Delete User.\n";
        cout << setw(37) << left << "" << "\t[4] Update User.\n";
        cout << setw(37) << left << "" << "\t[5] Find User.\n";
        cout << setw(37) << left << "" << "\t[6] Main Menue.\n";
        cout << setw(37) << left << "" << "===========================================\n";
        
        
        _performManageUsersMenueOption(
            (enManageUsersMenueOptions)_ReadManageUsersScreenOption());

    }



};

